package lu.mullerwegener.pdf.tools.pdftable;

/**
 * The differents text horizontal alignments for PdfCell
 * @author paquet
 *
 */
public enum PdfHorizontalAlignment{
	/**
	 * Align the text to the left of the cell 
	 */
	TEXT_LEFT, 
	/**
	 * Align the text to the right of the cell
	 */
	TEXT_RIGHT, 
	/**
	 * Align the text in the center of the cell
	 */
	TEXT_CENTER;
}
